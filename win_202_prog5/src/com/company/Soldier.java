package com.company;

//**********************************************************************************************************************
//*this is the Soldier class which derived from the Person class
//**********************************************************************************************************************

public class Soldier extends Person {

//**********************************************************************************************************************
//* Default constructor and call the base class's constructor
//**********************************************************************************************************************

    public Soldier() {
        super();
    }

//**********************************************************************************************************************
//*this is the copy constructor with two string argument passed in
//**********************************************************************************************************************

    public Soldier(String name, String description) {
        super(name, description);
    }

//**********************************************************************************************************************
//*this is the override display function
//**********************************************************************************************************************

    public void display() {
        super.display();
        System.out.println("Identification: Soldier");
    }
}
